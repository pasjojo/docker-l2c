function popup_ajouter(blockname) {
	el = document.getElementById('popup').className;
	blockoff = blockname+"-off";
	blockon = blockname+"-on";
	if ( el == blockoff || el == "") {
		document.getElementById('popup').className = blockon;
	}
	else
	{
		document.getElementById('popup').className = blockoff;
	}
}

